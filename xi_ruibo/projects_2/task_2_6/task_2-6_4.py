print("Ты стоишь на распутье.")
print("1. Пойти по лёгкому пути (удовольствия)")
print("2. Пойти по трудному пути (добродетели)")

choice = input("Твой выбор (1 или 2): ")

if choice == "1" or choice.lower() == "лёгкий":
    print("Ты выбрал лёгкий путь. Ты быстро получил удовольствие, но люди забыли о тебе. Твоя жизнь прошла впустую.")
elif choice == "2" or choice.lower() == "трудный":
    print("Ты выбрал трудный путь. Ты совершил подвиги, победил зло. Люди помнят и уважают тебя веками.")
else:
    print("Ты не сделал выбора. Ты остался стоять на месте. Никто не узнал о тебе.")